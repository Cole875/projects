public class Main {
    public static void main(String args[]) {
        //even fiz odd buzz
        for(int i = 0; i < 100; i++){
            if(i % 2 == 0){
                System.out.println("fiz " + i);
            }
            else {
                System.out.println("buzz " + i);
            }
        }
    }
}
